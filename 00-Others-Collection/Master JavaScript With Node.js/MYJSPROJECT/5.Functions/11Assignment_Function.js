/**
 * Write a JavaScript function expression that accepts a number as an argument and return its cube.
 */

const cube = function (number) {
  return Math.pow(number, 3); // Using Math.pow() to calculate cube
};

// Example usage:
console.log(cube(2)); // Output: 8 (2^3)
console.log(cube(3)); // Output: 27 (3^3)
