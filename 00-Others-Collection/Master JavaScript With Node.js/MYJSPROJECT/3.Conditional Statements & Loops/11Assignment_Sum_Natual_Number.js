//? Write a program to find sum of first 10 natural numbers.

// Initialize a variable to store the sum
let sum = 0;

// Iterate through the first 10 natural numbers
for (let i = 1; i <= 10; i++) {
  sum += i; // Add each number to the sum
}

// Print the sum
console.log("Sum of the first 10 natural numbers:", sum);
