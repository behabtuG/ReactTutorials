let str1 = "Friday";

let str2 = "Saturday";

let str3 = "Sunday";

console.log(str1 + " " + str2 + " " + str3); //Friday Saturday Sunday

console.log(str1.concat(" ", str2, " ", str3)); //Friday Saturday Sunday
